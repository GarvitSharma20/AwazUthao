// Helper to get major cities in a state/UT
export const getCitiesForState = (stateName: string): string[] => {
  const match = INDIAN_STATES_AND_CITIES.find(s => s.state.toLowerCase() === stateName.toLowerCase());
  return match ? Array.from(new Set(match.cities)).sort() : [];
};
